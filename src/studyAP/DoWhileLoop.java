package studyAP;

public class DoWhileLoop {
	public static void main(String[] args) {

		// print 1 to 10
		int i = 1;

		do {
			System.out.println(i);
			i++;
		} while (i <= 10);

		//print even numbers 1 to 10
		int j=2;
		do {
			System.out.println(j);
			j+=2;
		} while (j<=10);
		
			
		
	}
}
